
package com.pineapple.aivideosummary.service;

import org.springframework.stereotype.Component;

@Component
public class PromptTemplates {

    public String buildBatchSummaryPrompt(String batchContent) {
        return String.format(
                "你是一个专业的技术文档整理专家。请根据以下多个视频的字幕内容，提炼出核心知识点。\n\n" +
                        "【重要要求】\n" +
                        "1. 严格基于字幕内容，不准瞎编任何信息\n" +
                        "2. 去除口语化表达、重复内容、无关废话\n" +
                        "3. 只保留核心技术点、命令、概念、示例\n" +
                        "4. 用简洁清晰的语言概括\n" +
                        "5. 合并相同或相似的知识点\n" +
                        "6. 使用Markdown格式\n\n" +
                        "【输出要求】\n" +
                        "- 提取关键概念和操作步骤\n" +
                        "- 用列表呈现要点\n" +
                        "- 代码用代码块包裹（标注语言）\n" +
                        "- 保持逻辑清晰\n" +
                        "- 不要添加原文没有的内容\n" +
                        "- 每个知识点控制在2-3句话\n\n" +
                        "【字幕内容】\n%s\n\n" +
                        "请提炼核心知识点：",
                batchContent
        );
    }

    public String buildFinalIntegrationPrompt(String allBatchesContent) {
        return String.format(
                "你是一个专业的技术教程编写专家。请将以下内容整合成一份精简、系统化的学习笔记。\n\n" +
                        "【核心要求】\n" +
                        "1. 严格基于提供的内容，不准瞎编\n" +
                        "2. 去重：合并重复的知识点\n" +
                        "3. 结构化：按逻辑组织章节\n" +
                        "4. 精简：只保留核心内容，删除冗余\n" +
                        "5. 由易到难：从基础到高级\n" +
                        "6. 实用导向：突出实战应用\n\n" +
                        "【输出结构】\n" +
                        "请按以下结构组织内容（根据实际内容调整章节标题）：\n\n" +
                        "# [技术名称]速成笔记\n\n" +
                        "## 目录\n" +
                        "- 自动生成目录（使用Markdown链接）\n\n" +
                        "## 一、简介\n" +
                        "- 这是什么技术\n" +
                        "- 为什么需要它\n" +
                        "- 典型应用场景\n\n" +
                        "## 二、环境准备\n" +
                        "- 安装方法\n" +
                        "- 基本配置\n" +
                        "- 验证安装\n\n" +
                        "## 三、核心概念\n" +
                        "- 基本概念和术语\n" +
                        "- 架构或原理简述\n" +
                        "- 关键组件说明\n\n" +
                        "## 四、基础操作\n" +
                        "- 常用命令或API\n" +
                        "- 基本使用方法\n" +
                        "- 简单示例\n\n" +
                        "## 五、进阶特性\n" +
                        "- 高级功能\n" +
                        "- 最佳实践\n" +
                        "- 性能优化\n\n" +
                        "## 六、实战案例\n" +
                        "- 典型应用场景\n" +
                        "- 完整示例代码\n" +
                        "- 注意事项\n\n" +
                        "## 七、常见问题\n" +
                        "- 常见错误及解决\n" +
                        "- 调试技巧\n" +
                        "- 排查思路\n\n" +
                        "## 八、总结与展望\n" +
                        "- 核心要点回顾\n" +
                        "- 学习建议\n" +
                        "- 后续学习方向\n\n" +
                        "【格式要求】\n" +
                        "- 使用清晰的标题层级（# ## ###）\n" +
                        "- 重点内容加粗显示\n" +
                        "- 代码必须用代码块包裹，并标注语言\n" +
                        "- 适当使用表格对比不同方案\n" +
                        "- 每个知识点控制在2-3句话\n" +
                        "- 总长度控制在20-30页\n" +
                        "- 使用列表呈现步骤和要点\n" +
                        "- 关键命令单独成行\n\n" +
                        "【待整合内容】\n%s\n\n" +
                        "请开始编写学习笔记：",
                allBatchesContent
        );
    }
}